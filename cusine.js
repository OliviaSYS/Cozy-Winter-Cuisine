function update(idx) {
    document.getElementById('name').innerHTML = questions[idx]["name"];
    document.getElementById('description').innerHTML = questions[idx]["description"];
    document.getElementById('image').src = "Images/" + questions[idx]["imageUrl"];
    document.getElementById('ingredients').innerHTML = "";
    for (const ingredient of questions[idx]["ingredients"]) {
        document.getElementById('ingredients').innerHTML += ('<li>' + ingredient + '</li>');
    }
    document.getElementById('materials').innerHTML = "";
    for (const material of questions[idx]["materials"]) {
        document.getElementById('materials').innerHTML += ('<li>' + material + '</li>');
    }
    document.getElementById('steps').innerHTML = "";
    for (const step of questions[idx]["steps"]) {
        document.getElementById('steps').innerHTML += ('<li>' + step + '</li>');
    }
    document.getElementById('notes').innerHTML = "";
    for (const note of questions[idx]["notes"]) {
        document.getElementById('notes').innerHTML += ('<li>' + note + '</li>');
    }
}

function nextRecipe() {
    index = (index + 1) % questions.length;
}

document.getElementById("next").addEventListener("click", function () {
    nextRecipe();
    update(index);
});

